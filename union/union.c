#include <unistd.h>

int	main(int argc, char *argv[])
{
	int k;
	int i;

	if(argc != 3)
	{
		write(1, "\n", 1);
		return (0);
	}
		k = 0;
	while (argv[k] != '\0')
	{
		write(1, &argv[k], 1);
		i = k;
		while (i >= 0)
		{
			if(argv[i] == argv[k])
			{
				k++;
			}
			i--;
		}
		k++;
	}
		
}
